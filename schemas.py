from pydantic import BaseModel, ConfigDict, Field


# This is the base model for a post, which includes the common fields that are shared between the create and response models. It uses Pydantic's Field function to specify validation rules for each field, such as minimum and maximum length.
class PostBase(BaseModel):
    title : str = Field(min_length=1,max_length=100 )
    content : str = Field(min_length=1)
    author : str = Field(min_length=1,max_length=50)


# This is the model for creating a new post. It inherits from PostBase and doesn't add any new fields, but it can be used to validate the input data when creating a new post.
class PostCreate(PostBase):
    pass


# This is the model for the response when retrieving a post. It inherits from PostBase and adds additional fields such as id and date_posted, which are typically generated by the server when a new post is created. The model_config with from_attributes=True allows Pydantic to create an instance of PostResponse from an object that has attributes matching the field names, which can be useful when working with ORM models or other data sources.
class PostResponse(PostBase):
    model_config = ConfigDict(from_attributes=True) # This allows Pydantic to create an instance of PostResponse from an object that has attributes matching the field names, which can be useful when working with ORM models or other data sources.

    id : int
    date_posted : str